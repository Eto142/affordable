let sm_scrolltabs = new function() {
    this.update = (direction) => {
        //font: https://stackoverflow.com/questions/24635884/is-there-a-way-to-make-horizontal-scrolling-smoother
        let arrows = document.querySelectorAll('.arrow');
        let tabs_controller = document.querySelector('.tabs-controller ul');
        let tab_header_item = document.querySelector(".tab-header--item");
        let arrow_right = document.querySelector(".arrow.arrow-right");
        let arrow_left = document.querySelector(".arrow.arrow-left");
        //initial scroll amount
        let scrollAmount = 0;

        let slideTimer = setInterval(function(){
            if (direction == "right") {
                //scroll to left
                tabs_controller.scrollLeft += 10;
            } else {
                //scroll to right
                tabs_controller.scrollLeft -= 10;
            }
            //add 10px to scrollAmount
            scrollAmount += 10;

            //if scrolled amount is equal or bigger than the width of each tab element
            if (scrollAmount >= tab_header_item.offsetWidth){
                //remove arrow_right
                if (tabs_controller.offsetWidth - 42 == tabs_controller.scrollLeft) {
                    arrow_right.style.display = "none";
                } else {
                    arrow_right.style.display = "flex";
                }
                //remove arrow_left
                if (tabs_controller.scrollLeft != 0) {
                    arrow_left.style.display = "flex";
                } else {
                    arrow_left.style.display = "none";
                }
                //clear the setInterval
                window.clearInterval(slideTimer);
            }
        }, 15);
    }
};

 function scrollTabs() {
        let tabs_content = document.querySelectorAll('.tabs-content .tab');
        let tab_header_items = document.querySelectorAll('.tab-header--item');
        let metatrader = document.getElementById('metatrader-wrapper');
        const tab_indicator = document.querySelector('.tab-indicator');

        if (document.querySelector('.markets-tab.active')) {
            if(metatrader){
                metatrader.style.display = "none";
            }
        }

        for (let i=0; i< tab_header_items.length; i++) {

            tab_header_items[i].addEventListener('click', event => {

                /* only execute tab_active() if there's a ".table-tabs-wrapper" anywhere in the page */
                if (document.querySelector('.table-tabs-wrapper')) {
                    tab_active(tab_header_items[i]);
                }

                /*remove all "active" classes*/
                tab_header_items.forEach(e => e.classList.remove("active"));

               if (tab_header_items[i].classList.value.indexOf("markets-tab") > -1) {
                   if(metatrader){
                       metatrader.style.display = "none";
                   }

               } else {
                   if(metatrader){
                    metatrader.style.display = "flex";
                   }
               }

                //add "active" class to clicked element
                tab_header_items[i].classList.add("active");
                //move indicator
                tab_indicator.style.top = `calc(1rem + ${ i * 54 }px)`;

                tabs_content.forEach(e => e.classList.remove("active"));
                tabs_content[i].classList.add("active");
            });
        }
